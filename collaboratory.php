                <h2>In the Framework</h2>
                <p>The framework contains the following:</p>
                <ul>
                    <li>Core classes which provide the foundation for Coagmento Collaboratory giving an API for accessing user data (Bookmarks, Snippets, etc.) and research studies (Projects, Stages)</li>
                    <li>Web-services for accessing the core classes through HTTP requests</li>
                    <li>The Firefox plugin which uses the web-services as an example of practical usage of Coagmento Collaboratory</li>
                </ul>
