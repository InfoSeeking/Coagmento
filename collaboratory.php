                <h2>About Coagmento Collaboratory</h2>
                <p>Coagmento Collaboratory is a version of Coagmento designed to be:</p>
                <ul>
                    <li><strong>Modularized</strong> Coagmento Collaboratory splits up functionality into separate classes which are easier to understand and maintain</li>
                    <li><strong>Extendable</strong> We provide stateless web-services so you can develop your own tools on top of our framework.</li>
                    <li><strong>Documented</strong> Coagmento Collaboratory is well documented.</li>
                </ul>
